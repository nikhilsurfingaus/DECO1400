//Hold state of wether the checkbox is expanded for the drop down box
var expanded = false;
var expanded1 = false;
var expanded2 = false;
var expanded3 = false;
var expanded4 = false;
var expanded5 = false;
var expanded6 = false;
var expanded7 = false;

//Show checkbox display for expanded drop down box number 1
function showCheckboxes() {
  //When called get id for checkbox status
  var checkboxes = document.getElementById("checkboxes");
  //Not expanded the show to boxes
  if (!expanded) {
    checkboxes.style.display = "block";
    expanded = true;
  } else {
    //Not expanded then hide the boxes
    checkboxes.style.display = "none";
    expanded = false;
  }
}

